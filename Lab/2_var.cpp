// Write a C++ program that demonstrates the use of variables and constants. Create 
// variables of different data types and perform operations on them. 

#include<iostream>
using namespace std;

const int u=30;

int main()
{
   int a=10,b=20;
   char ch='d';
   float fl=20.20;
   
   cout<<"sum of "<<a<<"+"<<b<<"="<<a+b<<endl;
   cout<<"ch value : "<<ch<<endl;
   cout<<"sum of float"<<fl+a<<endl;
   cout<<"const value : "<<u<<endl;
}